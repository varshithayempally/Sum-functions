public class Sumfunction {
    public static int addnumbers(int a,int b) {
        return a+b;
    }
    public static void main(String[] args) {
        int num1=34;
        int num2=21;
        int sum=addnumbers(num1,num2);
        System.out.println("the sum of"+num1+"and"+num2+"is:"+sum);
    }
    
}
